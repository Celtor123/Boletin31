public class Supervisor {
   String nome;

        public Supervisor(String nome) {
            this.nome = nome;
        }

        public String getNome() {
            return nome;
        }

        public void setNome(String nome) {
            this.nome = nome;
        }

        @Override
        public String toString() {
            return "Supervisor{" + "nome=" + nome + '}';
        }
       
    
}

